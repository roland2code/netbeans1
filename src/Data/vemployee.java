package Data;

/**
 *
 * @author rolae
 */
public class vemployee extends vperson{
    String access;
    String login;
    String password;
    String status;

    public vemployee() {
    }

    public vemployee(String access, String login, String password, String status) {
        this.access = access;
        this.login = login;
        this.password = password;
        this.status = status;
    }

    public String getAccess() {
        return access;
    }

    public void setAccess(String access) {
        this.access = access;
    }

    public String getLogin() {
        return login;
    }

    public void setLogin(String login) {
        this.login = login;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }
    
    
    
}
