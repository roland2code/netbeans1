    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed
        // TODO add your handling code here:
        if (txtname.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Enter the Employee's Name");
            txtname.requestFocus();
            return;
        }
        if (txtlastname.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Enter the Employee's Last Name");
            txtlastname.requestFocus();
            return;
        }
        
        if (txtphone.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Enter the Employee's Phone Number");
            txtphone.requestFocus();
            return;
        }
        
        if (txtlogin.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Enter the Employee's User Name");
            txtlogin.requestFocus();
            return;
        }
        
        if (txtpassword.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Enter the Employee's password");
            txtpassword.requestFocus();
            return;
        }

        vemployee dts = new vemployee();
        femployee func = new femployee();

        dts.setName(txtname.getText());

        dts.setLastname(txtlastname.getText());
        dts.setAddress(txtaddress.getText());
        dts.setPhone(txtphone.getText());
        dts.setEmail(txtemail.getText());
        
        Calendar cal;
        int d,m,a;
        cal=dcbirthday.getCalendar();
        d=cal.get(Calendar.DAY_OF_MONTH);
        m=cal.get(Calendar.MONTH);
        a=cal.get(Calendar.YEAR) - 1900;
        dts.setBirthday(new Date(a,m,d));
        
        int seleccionado = cboaccess.getSelectedIndex();
        dts.setAccess((String) cboaccess.getItemAt(seleccionado));
        dts.setLogin(txtlogin.getText());
        dts.setPassword(txtpassword.getText());
        
        seleccionado = cbostatus.getSelectedIndex();
        dts.setStatus((String) cbostatus.getItemAt(seleccionado));


        if (accion.equals("guardar")){
            if (func.insertar(dts)){
                JOptionPane.showConfirmDialog(rootPane, "The Employee was Registered Satisfactorily");
                mostrar("");
                inhabilitar();
            }
        }
        else if (accion.equals("editar")){
            dts.setIdperson(Integer.parseInt(txtidperson.getText()));

            if (func.editar(dts)){
                JOptionPane.showConfirmDialog(rootPane, "The Employee was Successfully Edited");
                mostrar("");
                inhabilitar();

            }

        }
    }//GEN-LAST:event_btnguardarActionPerformed

