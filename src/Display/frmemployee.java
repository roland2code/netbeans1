    private void btneliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneliminarActionPerformed
        // TODO add your handling code here:
        if (!txtidperson.getText().equals("")) {
            int confirmacion = JOptionPane.showConfirmDialog(rootPane, "Are You Sure You Want to Eliminate The Employee?","Confirm",2);

            if (confirmacion==0) {
                femployee func = new femployee ();
                vemployee dts= new vemployee();

                dts.setIdperson(Integer.parseInt(txtidperson.getText()));
                func.eliminar(dts);
                mostrar("");
                inhabilitar();

            }
        }
    }//GEN-LAST:event_btneliminarActionPerformed

