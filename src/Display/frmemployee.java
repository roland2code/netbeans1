    private void tablalistadoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablalistadoMouseClicked
        // TODO add your handling code here:
        btnguardar.setText("Edit");
        habilitar();
        btneliminar.setEnabled(true);
        accion="editar";

        int fila = tablalistado.rowAtPoint(evt.getPoint());

        txtidperson.setText(tablalistado.getValueAt(fila, 0).toString());
        txtname.setText(tablalistado.getValueAt(fila, 1).toString());
        txtlastname.setText(tablalistado.getValueAt(fila, 2).toString());
        txtaddress.setText(tablalistado.getValueAt(fila, 3).toString());
        txtphone.setText(tablalistado.getValueAt(fila, 4).toString());
        txtemail.setText(tablalistado.getValueAt(fila, 5).toString());
        dcbirthday.setDate(Date.valueOf(tablalistado.getValueAt(fila, 6).toString()));
        cboaccess.setSelectedItem(tablalistado.getValueAt(fila,7).toString());
        txtlogin.setText(tablalistado.getValueAt(fila, 8).toString());
        txtpassword.setText(tablalistado.getValueAt(fila, 9).toString());
        cbostatus.setSelectedItem(tablalistado.getValueAt(fila,10).toString());
        

    }//GEN-LAST:event_tablalistadoMouseClicked

